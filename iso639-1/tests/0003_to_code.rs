extern crate iso639_1;
use iso639_1::{to_iso639_3, Iso639v1ErrorKind};

#[test]
fn get_iso639v3_0001_not_match() {
    let result = to_iso639_3("frite");
    assert!(result.is_err() == true);
    let error = result.err().unwrap();
    let message = error.inner.to_string();
    assert_eq!(message, "not found frite".to_string());
    let kind = error.kind();
    assert_eq!(kind, &Iso639v1ErrorKind::NotFoundTo("frite".to_string()));
}

